void VulkanPlayground::run() {
    initWindow();
    initVulkan();
    compute();
    mainLoop();
    cleanup();
}

void VulkanPlayground::initWindow() {
    glfwInit();
    glfwWindowHint(GLFW_CLIENT_API, GLFW_NO_API);
    glfwWindowHint(GLFW_RESIZABLE, GLFW_TRUE);
    window = glfwCreateWindow(WIDTH, HEIGHT, "Vulkan", nullptr, nullptr);
    glfwSetWindowUserPointer(window, this);
    glfwSetFramebufferSizeCallback(window, framebufferResizeCallback);
}

void VulkanPlayground::framebufferResizeCallback(GLFWwindow* window, int width, int height) {
    auto app = reinterpret_cast<VulkanPlayground*>(glfwGetWindowUserPointer(window));
    app->framebufferResized = true;
}

